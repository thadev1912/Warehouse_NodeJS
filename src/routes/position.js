var express = require('express');
var router = express.Router();
const position = require('../app/Controllers/PositionController');
const validate=require('../request/PostionRequest');
const Auth =require('../app/middlewares/authenticatetion');
const Permision =require('../app/middlewares/authorization');
router.get('/listPosition',Auth.checkAuth,Permision.checkPermision,position.index);
router.post('/storePosition',Auth.checkAuth,Permision.checkPermision,validate.checkValidate,position.create);
router.get('/editPosition',Auth.checkAuth,Permision.checkPermision,position.edit);
router.put('/updatePosition/:id',Auth.checkAuth,Permision.checkPermision,validate.checkValidate,position.update);
router.delete('/deletePosition',Auth.checkAuth,Permision.checkPermision,position.destroy);
module.exports = router;
